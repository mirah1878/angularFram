public class Route {
    private String routesName;
    private String routesExtension;
    private String routeContent;
    private String routeContentChildren;
    private String routeContentImport;
    private String routeExtension;
    private String routeSavePath;

    public String getRoutesName() {
        return routesName;
    }

    public void setRoutesName(String routesName) {
        this.routesName = routesName;
    }

    public String getRoutesExtension() {
        return routesExtension;
    }

    public void setRoutesExtension(String routesExtension) {
        this.routesExtension = routesExtension;
    }

    public String getRouteContent() {
        return routeContent;
    }

    public void setRouteContent(String routeContent) {
        this.routeContent = routeContent;
    }

    public String getRouteContentChildren() {
        return routeContentChildren;
    }

    public void setRouteContentChildren(String routeContentChildren) {
        this.routeContentChildren = routeContentChildren;
    }

    public String getRouteExtension() {
        return routeExtension;
    }

    public void setRouteExtension(String routeExtension) {
        this.routeExtension = routeExtension;
    }

    public String getRouteSavePath() {
        return routeSavePath;
    }

    public void setRouteSavePath(String routeSavePath) {
        this.routeSavePath = routeSavePath;
    }

    public String getRouteContentImport() {
        return routeContentImport;
    }

    public void setRouteContentImport(String routeContentImport) {
        this.routeContentImport = routeContentImport;
    }

}
