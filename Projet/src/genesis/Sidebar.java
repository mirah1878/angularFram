package genesis;

public class Sidebar {
    private String sidebarName;
    private String sidebarExtension;
    private String sidebarContent;
    private String sidebarContentChildren;
    private String sidebarSavePath;

    public String getSidebarName() {
        return sidebarName;
    }

    public void setSidebarName(String sidebarName) {
        this.sidebarName = sidebarName;
    }

    public String getSidebarExtension() {
        return sidebarExtension;
    }

    public void setSidebarExtension(String sidebarExtension) {
        this.sidebarExtension = sidebarExtension;
    }

    public String getSidebarContent() {
        return sidebarContent;
    }

    public void setSidebarContent(String sidebarContent) {
        this.sidebarContent = sidebarContent;
    }

    public String getSidebarContentChildren() {
        return sidebarContentChildren;
    }

    public void setSidebarContentChildren(String sidebarContentChildren) {
        this.sidebarContentChildren = sidebarContentChildren;
    }

    public String getSidebarSavePath() {
        return sidebarSavePath;
    }

    public void setSidebarSavePath(String sidebarSavePath) {
        this.sidebarSavePath = sidebarSavePath;
    }

}
