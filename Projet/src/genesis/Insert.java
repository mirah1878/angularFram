package genesis;

import java.util.HashMap;

public class Insert {
    private String inputComponent;
    private String textareaComponent;
    private String listComponent;
    private HashMap<String, String> types;
    private HashMap<String, String> typesComponent;

    public String getInputComponent() {
        return inputComponent;
    }

    public void setInputComponent(String inputComponent) {
        this.inputComponent = inputComponent;
    }

    public String getTextareaComponent() {
        return textareaComponent;
    }

    public void setTextareaComponent(String textareaComponent) {
        this.textareaComponent = textareaComponent;
    }

    public String getListComponent() {
        return listComponent;
    }

    public void setListComponent(String listComponent) {
        this.listComponent = listComponent;
    }

    public HashMap<String, String> getTypes() {
        return types;
    }

    public void setTypes(HashMap<String, String> types) {
        this.types = types;
    }

    public HashMap<String, String> getTypesComponent() {
        return typesComponent;
    }

    public void setTypesComponent(HashMap<String, String> typesComponent) {
        this.typesComponent = typesComponent;
    }

}
